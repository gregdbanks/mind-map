import type { Node, Link, NodeSize } from '../types/mindMap';

export interface MindMapTemplate {
  id: string;
  name: string;
  description: string;
  icon: string;
  nodes: Node[];
  links: Link[];
}

function node(id: string, text: string, parent: string | null, color?: string, textColor?: string, size?: NodeSize): Node {
  return { id, text, collapsed: false, parent, color, textColor, size };
}

function link(source: string, target: string): Link {
  return { source, target };
}

